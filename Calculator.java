package week1.day2;

public class Calculator {
	public int addition(int a, int b, int c) {
		int sum;
		sum=a+b+c;
		return(sum);
	}
	public int subtraction(int a, int b) {
		int sub;
		sub=a-b;
		return(sub);	
	}
	public double multiply(double a, double b) {
		double mul;
		mul=a*b;
		return(mul);	
	}
	public float divide(float a, float b) {
		float div;
		div=a/b;
		return(div);	
	}
}
